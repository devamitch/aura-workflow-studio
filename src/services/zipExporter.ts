// ── Main export function ──────────────────────────────────────────────────────
export async function exportProjectZip(
  nodes: PipelineNode[],
  edges: PipelineEdge[],
  projectType: ExportProjectType,
  workflowName = "Aura Workflow"
): Promise<void> {
  const zip = new JSZip();
  const { plan } = compileGraph(nodes, edges);

  const workflowJson = JSON.stringify({ nodes, edges, plan }, null, 2);
  const envVars = collectEnvVars(nodes);
  const masterPrompt = generateMasterPrompt(nodes, edges, workflowName);
  const slugName = workflowName.toLowerCase().replace(/\s+/g, "-");

  // ── Root files ──────────────────────────────────────────────────────────────
  zip.file("workflow.json", workflowJson);
  zip.file(".env.template", genEnvTemplate(envVars));
  zip.file("README.md", genReadme(workflowName, projectType, masterPrompt));
  zip.file("master-prompt.txt", masterPrompt);
  zip.file("start.sh", genStartScript());
  zip.file("docker-compose.yml", genDockerCompose(workflowName));

  zip.file(
    "package.json",
    JSON.stringify(
      {
        name: slugName,
        version: "1.0.0",
        description: `Generated by Aura Engine — ${workflowName}`,
        scripts: {
          dev: "concurrently \"npm run dev:backend\" \"npm run dev:frontend\"",
          "dev:backend": "cd backend && node index.js",
          "dev:frontend": "cd frontend && npm run dev",
          build: "cd frontend && npm run build",
          start: "node backend/index.js",
        },
        dependencies: {
          express: "^4.18.0",
          cors: "^2.8.5",
          dotenv: "^16.0.0",
          concurrently: "^8.0.0",
          ...(projectType === "telegram-bot" ? { "node-telegram-bot-api": "^0.64.0" } : {}),
          ...(projectType === "discord-bot" ? { "discord.js": "^14.0.0" } : {}),
          ...(projectType === "whatsapp-bot" ? { twilio: "^4.0.0" } : {}),
        },
      },
      null,
      2
    )
  );

  // ── Backend ─────────────────────────────────────────────────────────────────
  const backend = zip.folder("backend")!;
  backend.file("index.js", genBackendIndex(workflowJson));
  backend.file(
    "package.json",
    JSON.stringify({ name: `${slugName}-backend`, version: "1.0.0", main: "index.js", scripts: { start: "node index.js", dev: "nodemon index.js" }, dependencies: { express: "^4.18.0", cors: "^2.8.5", dotenv: "^16.0.0" } }, null, 2)
  );
  backend.file(
    "Dockerfile",
    `FROM node:20-alpine\nWORKDIR /app\nCOPY package.json .\nRUN npm install\nCOPY . .\nEXPOSE 3000\nCMD ["node", "index.js"]`
  );

  // ── Frontend ────────────────────────────────────────────────────────────────
  const frontend = zip.folder("frontend")!;
  frontend.file("src/App.jsx", genFrontendApp());
  frontend.file("src/main.jsx", `import React from 'react';\nimport ReactDOM from 'react-dom/client';\nimport App from './App.jsx';\n\nReactDOM.createRoot(document.getElementById('root')).render(<React.StrictMode><App /></React.StrictMode>);`);
  frontend.file("index.html", `<!DOCTYPE html>\n<html>\n<head><meta charset="UTF-8"><title>${workflowName}</title></head>\n<body><div id="root"></div><script type="module" src="/src/main.jsx"></script></body>\n</html>`);
  frontend.file("package.json", JSON.stringify({ name: `${slugName}-frontend`, version: "1.0.0", type: "module", scripts: { dev: "vite", build: "vite build" }, dependencies: { react: "^18.2.0", "react-dom": "^18.2.0" }, devDependencies: { "@vitejs/plugin-react": "^4.2.0", vite: "^5.0.0" } }, null, 2));
  frontend.file("vite.config.js", `import { defineConfig } from 'vite';\nimport react from '@vitejs/plugin-react';\n\nexport default defineConfig({ plugins: [react()], server: { proxy: { '/webhook': 'http://localhost:3000' } } });`);
  frontend.file(
    "Dockerfile",
    `FROM node:20-alpine\nWORKDIR /app\nCOPY package.json .\nRUN npm install\nCOPY . .\nEXPOSE 5173\nCMD ["npm", "run", "dev", "--", "--host"]`
  );

  // ── Bot (if applicable) ─────────────────────────────────────────────────────
  if (["telegram-bot", "discord-bot", "whatsapp-bot"].includes(projectType)) {
    const bot = zip.folder("bot")!;
    bot.file("index.js", genBotIndex(projectType, workflowJson));
    bot.file("package.json", JSON.stringify({ name: `${slugName}-bot`, version: "1.0.0", scripts: { start: "node index.js" } }, null, 2));
  }

  // ── Shared ──────────────────────────────────────────────────────────────────
  const shared = zip.folder("shared")!;
  shared.file(
    "workflow-types.ts",
    `// Auto-generated workflow types\nexport const WORKFLOW_NAME = '${workflowName}';\nexport const NODE_COUNT = ${nodes.length};\nexport const EDGE_COUNT = ${edges.length};\n`
  );

  // ── Download ────────────────────────────────────────────────────────────────
  const blob = await zip.generateAsync({ type: "blob", compression: "DEFLATE" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `${slugName}-aura-export.zip`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
