const BOT_PROJECT_TYPES: ExportProjectType[] = [
  "telegram-bot",
  "discord-bot",
  "whatsapp-bot",
];

function lines(parts: string[]): string {
  return `${parts.join("\n")}\n`;
}

function slugify(name: string): string {
  return name
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function collectEnvVars(
  nodes: PipelineNode[],
  projectType: ExportProjectType,
): Record<string, string> {
  const env: Record<string, string> = {
    PORT: "3000",
    NODE_ENV: "development",
    OPENAI_API_KEY: "",
    ANTHROPIC_API_KEY: "",
    GEMINI_API_KEY: "",
  };

  if (projectType === "fullstack-docker" || projectType === "react-spa") {
    env.VITE_API_URL = "http://localhost:3000";
  }

  if (projectType === "telegram-bot") {
    env.TELEGRAM_BOT_TOKEN = "";
    env.BACKEND_URL = "http://localhost:3000";
  }

  if (projectType === "discord-bot") {
    env.DISCORD_BOT_TOKEN = "";
    env.BACKEND_URL = "http://localhost:3000";
  }

  if (projectType === "whatsapp-bot") {
    env.TWILIO_ACCOUNT_SID = "";
    env.TWILIO_AUTH_TOKEN = "";
    env.TWILIO_WHATSAPP_NUMBER = "whatsapp:+14155238886";
    env.BACKEND_URL = "http://localhost:3000";
    env.BOT_PORT = "3100";
  }

  if (projectType === "wordpress-plugin") {
    env.WP_BRIDGE_API_KEY = "";
  }

  for (const node of nodes) {
    const def = getNodeDefinition(node.data.nodeType ?? "");
    if (!def?.requiredCredentials) continue;
    for (const cred of def.requiredCredentials) {
      const key = `${(node.data.label ?? node.data.nodeType ?? node.id)
        .toUpperCase()
        .replace(/\s+/g, "_")}_${cred.key.toUpperCase()}`;
      env[key] = cred.secret ? "" : "your_value_here";
    }
  }

  return env;
}

function genEnvTemplate(envVars: Record<string, string>): string {
  return lines([
    "# Aura Engine Environment Variables",
    "# Copy this file to .env and fill your values",
    "",
    ...Object.entries(envVars).map(([k, v]) => `${k}=${v}`),
  ]);
}

function projectLabel(projectType: ExportProjectType): string {
  switch (projectType) {
    case "telegram-bot":
      return "Telegram Bot";
    case "discord-bot":
      return "Discord Bot";
    case "whatsapp-bot":
      return "WhatsApp Bot";
    case "react-spa":
      return "React SPA";
    case "node-api":
      return "Node API";
    case "fullstack-docker":
      return "Fullstack Docker";
    case "wordpress-plugin":
      return "WordPress Plugin";
    default:
      return projectType;
  }
}

function genReadme(
  workflowName: string,
  projectType: ExportProjectType,
  masterPrompt: string,
): string {
  const isBot = BOT_PROJECT_TYPES.includes(projectType);
  const includesFrontend =
    projectType === "react-spa" || projectType === "fullstack-docker";
  const includesBackend = projectType !== "react-spa";
  const includesWordPress = projectType === "wordpress-plugin";

  const structure = ["workflow.json", ".env.template", "master-prompt.txt"];
  if (includesBackend) structure.push("backend/");
  if (includesFrontend) structure.push("frontend/");
  if (isBot) structure.push("bot/");
  if (includesWordPress) structure.push("wordpress-plugin/");

  const nextSteps: string[] = ["cp .env.template .env", "npm install", "npm run dev"];
  if (projectType === "fullstack-docker" || isBot) {
    nextSteps.push("docker-compose up --build");
  }
  if (projectType === "wordpress-plugin") {
    nextSteps.push(
      "Install plugin from wordpress-plugin/ in your WordPress instance",
    );
  }

  return lines([
    `# ${workflowName}`,
    "",
    "Generated by Aura Engine visual workflow orchestrator.",
    "",
    "## Project Type",
    projectLabel(projectType),
    "",
    "## Quick Start",
    "```bash",
    ...nextSteps,
    "```",
    "",
    "## Project Structure",
    "```",
    ...structure.map((entry) => `- ${entry}`),
    "```",
    "",
    "## Master Prompt",
    "```text",
    masterPrompt,
    "```",
  ]);
}

function genStartScript(projectType: ExportProjectType): string {
  const runCmd = BOT_PROJECT_TYPES.includes(projectType)
    ? 'concurrently "npm run dev:backend" "npm run dev:bot"'
    : projectType === "fullstack-docker"
      ? 'concurrently "npm run dev:backend" "npm run dev:frontend"'
      : projectType === "react-spa"
        ? "npm run dev:frontend"
        : "npm run dev:backend";

  return lines([
    "#!/bin/bash",
    "set -e",
    "",
    "echo \"Starting exported Aura project...\"",
    "",
    "if [ ! -f .env ]; then",
    "  cp .env.template .env",
    "  echo \"Created .env from template. Fill credentials and rerun.\"",
    "  exit 1",
    "fi",
    "",
    "npm install",
    runCmd,
  ]);
}

