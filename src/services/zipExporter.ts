function genBackendIndex(workflowJson: string): string {
  return lines([
    "const express = require('express');",
    "const cors = require('cors');",
    "const { GoogleGenAI } = require('@google/genai');",
    "require('dotenv').config();",
    "",
    "const app = express();",
    "app.use(cors());",
    "app.use(express.json());",
    "",
    "const workflow = " + workflowJson + ";",
    "",
    "const GEMINI_MODELS = ['gemini-3.0-flash', 'gemini-2.5-flash-preview-04-17', 'gemini-1.5-flash'];",
    "const GEMINI_API_KEY = process.env.GEMINI_API_KEY || '';",
    "const GEMINI_CLIENT = GEMINI_API_KEY ? new GoogleGenAI({ apiKey: GEMINI_API_KEY }) : null;",
    "",
    "app.get('/health', (_req, res) => {",
    "  res.json({ status: 'ok', nodes: workflow.nodes?.length ?? 0, edges: workflow.edges?.length ?? 0 });",
    "});",
    "",
    "app.get('/workflow', (_req, res) => {",
    "  res.json({ nodes: workflow.nodes, edges: workflow.edges, plan: workflow.plan });",
    "});",
    "",
    "app.post(['/webhook', '/run'], async (req, res) => {",
    "  try {",
    "    const result = await executeWorkflow(req.body ?? {});",
    "    res.json({ success: true, result });",
    "  } catch (err) {",
    "    const message = err instanceof Error ? err.message : 'Workflow execution failed';",
    "    res.status(500).json({ success: false, error: message });",
    "  }",
    "});",
    "",
    "function nodeById(id) {",
    "  return (workflow.nodes || []).find((n) => n.id === id);",
    "}",
    "",
    "function incomingFor(nodeId) {",
    "  return (workflow.edges || []).filter((e) => e.target === nodeId).map((e) => e.source);",
    "}",
    "",
    "async function callGemini(prompt) {",
    "  if (!GEMINI_CLIENT) return { text: prompt, model: null, fallback: true, reason: 'No GEMINI_API_KEY' };",
    "",
    "  for (const model of GEMINI_MODELS) {",
    "    try {",
    "      const response = await GEMINI_CLIENT.models.generateContent({",
    "        model,",
    "        contents: [{ role: 'user', parts: [{ text: prompt }] }],",
    "        config: { temperature: 0.5, maxOutputTokens: 1024 }",
    "      });",
    "      const text = response?.text ?? '';",
    "      if (text) return { text, model, fallback: false };",
    "    } catch (_e) {",
    "      // try next fallback",
    "    }",
    "  }",
    "",
    "  return { text: prompt, model: null, fallback: true, reason: 'All Gemini models failed' };",
    "}",
    "",
    "function safeString(value) {",
    "  if (value == null) return '';",
    "  if (typeof value === 'string') return value;",
    "  try { return JSON.stringify(value); } catch { return String(value); }",
    "}",
    "",
    "function evalCondition(condition, input, context) {",
    "  try {",
    "    const fn = new Function('input', 'context', 'return Boolean(' + condition + ');');",
    "    return Boolean(fn(input, context));",
    "  } catch (_e) {",
    "    return false;",
    "  }",
    "}",
    "",
    "async function executeNode(node, input, context) {",
    "  const nodeType = String(node?.data?.nodeType || node?.type || '').toLowerCase();",
    "  const config = node?.data?.config || {};",
    "",
    "  if (nodeType.includes('trigger') || nodeType === 'custominput' || nodeType === 'webhook' || nodeType === 'timer') return input;",
    "",
    "  if (nodeType === 'if' || nodeType === 'filter') {",
    "    const condition = String(config.condition || node?.data?.condition || 'true');",
    "    return { passed: evalCondition(condition, input, context), input };",
    "  }",
    "",
    "  if (nodeType === 'http' || nodeType === 'api') {",
    "    const url = String(config.url || node?.data?.url || '');",
    "    const method = String(config.method || node?.data?.method || 'GET').toUpperCase();",
    "    if (!url) return { error: 'Missing URL', input };",
    "",
    "    const response = await fetch(url, {",
    "      method,",
    "      headers: { 'Content-Type': 'application/json' },",
    "      body: method === 'GET' ? undefined : JSON.stringify(input)",
    "    });",
    "    const text = await response.text();",
    "    try {",
    "      return JSON.parse(text);",
    "    } catch (_e) {",
    "      return text;",
    "    }",
    "  }",
    "",
    "  if (nodeType === 'llm' || nodeType === 'aiagent' || nodeType === 'ragpipeline') {",
    "    const promptBase = config.prompt || node?.data?.prompt || node?.data?.systemPrompt || 'Process workflow input';",
    "    const prompt = String(promptBase) + '\\n\\nINPUT:\\n' + safeString(input);",
    "    return await callGemini(prompt);",
    "  }",
    "",
    "  if (nodeType === 'prompttemplate' || nodeType === 'text') {",
    "    const template = String(config.template || node?.data?.text || node?.data?.prompt || '');",
    "    return { text: template, input };",
    "  }",
    "",
    "  if (nodeType === 'set' || nodeType === 'transform') {",
    "    return { ...(typeof input === 'object' && input ? input : {}), ...(typeof config === 'object' && config ? config : {}) };",
    "  }",
    "",
    "  return input;",
    "}",
    "",
    "async function executeWorkflow(input) {",
    "  const results = {};",
    "  const steps = Array.isArray(workflow.plan?.steps)",
    "    ? workflow.plan.steps.map((s) => s.nodeId)",
    "    : (workflow.nodes || []).map((n) => n.id);",
    "",
    "  for (const nodeId of steps) {",
    "    const node = nodeById(nodeId);",
    "    if (!node) continue;",
    "",
    "    const incoming = incomingFor(nodeId);",
    "    const nodeInput = incoming.length === 0",
    "      ? input",
    "      : incoming.length === 1",
    "        ? results[incoming[0]]",
    "        : incoming.reduce((acc, key) => ({ ...acc, [key]: results[key] }), {});",
    "",
    "    results[nodeId] = await executeNode(node, nodeInput, { input, results, workflow });",
    "  }",
    "",
    "  const lastNodeId = steps[steps.length - 1];",
    "  return {",
    "    output: lastNodeId ? results[lastNodeId] : input,",
    "    results,",
    "  };",
    "}",
    "",
    "const PORT = process.env.PORT || 3000;",
    "app.listen(PORT, () => {",
    "  console.log('Aura backend running on port ' + PORT);",
    "});",
  ]);
}

