function genStartScript(): string {
  return `#!/bin/bash
set -e

echo "🚀 Starting Aura Engine Workflow App..."

# Check for .env
if [ ! -f .env ]; then
  echo "⚠️  .env not found. Copying from template..."
  cp .env.template .env
  echo "✏️  Please fill in your credentials in .env then re-run this script."
  exit 1
fi

# Install dependencies
echo "📦 Installing dependencies..."
npm install

# Start all services
echo "▶️  Starting services..."
if command -v concurrently &> /dev/null; then
  concurrently "npm run dev:backend" "npm run dev:frontend"
else
  npm run dev
fi
`;
}

